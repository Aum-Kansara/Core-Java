public class SwitchCases{
    public static void main(String args[]){
        String name="Aum Kansara";
        switch(name){
            case "Name1" -> System.out.println("Hello Name1");
            case "Name2" -> System.out.println("Hello Name1");
            case "Aum Kansara" -> System.out.println("Hello Aum Kansara");
            case "Name4" -> System.out.println("Hello Name1");
        }
    }
}